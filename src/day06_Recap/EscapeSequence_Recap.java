package day06_Recap;

public class EscapeSequence_Recap {

    public static void main(String[] args) {

        System.out.println("Hello Everyone\nHello Batch 21\nToday I have learned a programming language \"Java\"");

        System.out.println("My favorite quote is: \n\t\"YourFavoriteQuote\"\n\t\t\t\t- Name");

    }
}
/*
 Escape sequences: for text
        \n: append new line
        \t: tab space, paragraph space
        \\: \
        \": double quote
    Hello Everyone
    Hello Batch 21
    Today I have learned a programming language "Java"
    -------------------------------------------------------
    My favorite quote is:
        "YourFavoriteQuote"
                    - Name
 */