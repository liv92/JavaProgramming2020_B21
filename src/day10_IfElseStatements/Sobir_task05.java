package day10_IfElseStatements;

public class Sobir_task05 {

    public static void main(String[] args) {

        int childHeight = 50;
        System.out.println("This year she is "+ childHeight--+" inches.");
        System.out.println("Last year she was "+childHeight+++" inches.");
        System.out.println("Next year she should be "+ ++childHeight+" inches.");

    }
}

