package day03_Variables;

/*
declare variables:
    DataType name = Data;
 */

public class PrimativeDataTypes {

    public static void main(String[] args) {
      // score is 85

        byte score = 85;

    //compiler defaults to int even if using long. In long end data string with 'L'
        long number1 = 999999999L;

        double decimal1 = 0.5;
        float decimal2 = 0.5F; // compiler defaults to double. Add 'F' to end of data string.

        System.out.println(decimal2);



    }

}
