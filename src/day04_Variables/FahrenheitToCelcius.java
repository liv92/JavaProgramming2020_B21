package day04_Variables;

public class FahrenheitToCelcius {
    public static void main(String[] args) {
        /*
        Write a Java program to convert Fahrenheit to Celcius F=9*C/5+32
         */
        int C = 22;
        int F = 9*C/5+32;
        int fahrenheit = F;

        System.out.println(fahrenheit);


    }

}
