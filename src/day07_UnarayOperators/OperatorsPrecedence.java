package day07_UnarayOperators;

public class OperatorsPrecedence {

    public static void main(String[] args) {

        int x = 2 + 3 * 2 / 1;
        System.out.println(x);

        int y = (2 + 3) * 2;
        System.out.println(y);

        int w = 78 / 2 * 2 + 3 - 5 % 5;
        System.out.println(w);



    }
}
